#Find relations between 2 mystery numbers
n1 = int(input("Enter mystery number 1:"))
n2 = int(input("Enter mystery number 2:"))

print(f"{n1}<{n2} is {n1<n2}")
print(f"{n1}<={n2} is {n1<=n2}")
print(f"{n1}>{n2} is {n1>n2}")
print(f"{n1}>={n2} is {n1>=n2}")
print(f"{n1}=={n2} is {n1==n2}")
print(f"{n1}!={n2} is {n1!=n2}")